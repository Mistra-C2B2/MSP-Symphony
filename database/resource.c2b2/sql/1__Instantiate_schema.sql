CREATE OR REPLACE VIEW calculationresultslice
            (id, baselineversion_id, calculationname, timestamp, owner, ispurged, haschanges) AS
SELECT cr.cares_id                                   AS id,
       cr.cares_bver_id                              AS baselineversion_id,
       cr.cares_calculationname                      AS calculationname,
       cr.cares_timestamp                            AS "timestamp",
       cr.cares_owner                                AS owner,
       cr.cares_geotiff IS NULL                      AS ispurged,
       (((s.changes ->> 'baseChanges'::text)::json) ->> 'PRESSURE'::text) IS NOT NULL OR
       (((s.changes ->> 'baseChanges'::text)::json) ->> 'ECOSYSTEM'::text) IS NOT NULL OR
       ((SELECT COUNT(ac.key) AS count
         FROM JSON_EACH((s.changes ->> 'areaChanges'::text)::json) ac(key, value)
         WHERE NOT ac.value::text = '{}'::text)) > 0 AS haschanges
FROM symphony.scenariosnapshot s
         JOIN symphony.calculationresult cr ON cr.scenariosnapshot_id = s.id;

EXECUTE FORMAT ('ALTER VIEW calculationresultslice OWNER TO %s', pgAppAgent);
EXECUTE FORMAT ('GRANT SELECT ON calculationresultslice TO %s', pgAppAgent);

